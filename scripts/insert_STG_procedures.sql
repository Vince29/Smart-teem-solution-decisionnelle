CREATE OR REPLACE PROCEDURE INSERT_CHAMBRE(
  NO_CHAMBRE_ARRAY ARRAY,
  NOM_CHAMBRE_ARRAY ARRAY,
  NO_ETAGE_ARRAY ARRAY,
  NOM_BATIMENT_ARRAY ARRAY,
  TYPE_CHAMBRE_ARRAY ARRAY,
  PRIX_JOUR_ARRAY ARRAY,
  DT_CREATION_ARRAY ARRAY
)
RETURNS STRING
LANGUAGE SQL
AS
$$
DECLARE
  i INTEGER DEFAULT 0;
  rows_count INTEGER;
BEGIN
  rows_count := ARRAY_SIZE(:NO_CHAMBRE_ARRAY);
  
  INSERT INTO CHAMBRE
  SELECT 
    :NO_CHAMBRE_ARRAY[i.value]::INTEGER,
    :NOM_CHAMBRE_ARRAY[i.value]::VARCHAR,
    :NO_ETAGE_ARRAY[i.value]::BYTEINT,
    :NOM_BATIMENT_ARRAY[i.value]::VARCHAR,
    :TYPE_CHAMBRE_ARRAY[i.value]::VARCHAR,
    :PRIX_JOUR_ARRAY[i.value]::SMALLINT,
    :DT_CREATION_ARRAY[i.value]::DATE
  FROM TABLE(FLATTEN(INPUT => ARRAY_GENERATE_RANGE(0, :rows_count))) i;
  
  RETURN :rows_count || ' CHAMBRE(S) insérée(s)';
END;
$$;

